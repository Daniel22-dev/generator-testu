import fs from 'node:fs';
import path from 'node:path';
import * as espree from 'espree';

const dir = path.resolve('src/js');
const names = new Set();
function addPattern(node) {
  if (!node) return;
  if (node.type === 'Identifier') names.add(node.name);
  else if (node.type === 'ObjectPattern') node.properties.forEach(p => addPattern(p.value || p.argument));
  else if (node.type === 'ArrayPattern') node.elements.forEach(addPattern);
  else if (node.type === 'AssignmentPattern') addPattern(node.left);
  else if (node.type === 'RestElement') addPattern(node.argument);
}
for (const file of fs.readdirSync(dir).filter(x => x.endsWith('.js')).sort()) {
  const source = fs.readFileSync(path.join(dir, file), 'utf8');
  const ast = espree.parse(source, {ecmaVersion:2022, sourceType:'script'});
  for (const node of ast.body) {
    if ((node.type === 'FunctionDeclaration' || node.type === 'ClassDeclaration') && node.id) names.add(node.id.name);
    if (node.type === 'VariableDeclaration') node.declarations.forEach(d => addPattern(d.id));
  }
}
const lines = [...names].sort().map(name => `  ${JSON.stringify(name)}: 'writable',`);
const out = `// AUTO-GENERATED by scripts/generate-eslint-globals.mjs. Do not edit manually.\nexport const projectGlobals = {\n${lines.join('\n')}\n};\n`;
fs.writeFileSync('eslint-globals.generated.mjs', out, 'utf8');
console.log(`✅  ESLint globály vygenerovány (${names.size} názvů).`);
